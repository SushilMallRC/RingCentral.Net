namespace RingCentral
{
    public class DelegateExtensionInfo
    {
        /// <summary>
        /// Canonical URI of an extension
        /// </summary>
        public string uri { get; set; }

        /// <summary>
        /// Extension name
        /// </summary>
        public string name { get; set; }

        /// <summary>
        /// Number of an extension
        /// </summary>
        public string extensionNumber { get; set; }
    }
}